// generate-rpc-index.js — regenerate docs/RPC_INDEX_GENERATED.md from the
// compiled bundle (Q-10, WORLD_CLASS_SOCIAL_FRIENDS_GROUPS_ARCHITECTURE.md
// §17.4): the hand-written COMPLETE_RPC_REFERENCE.md documented ~18 social
// RPCs while the live server registered 70+, and listed RPCs that don't
// exist. This script makes the RPC inventory a build artifact instead of a
// hand-edited list, so it can never drift again.
//
// Usage:  npm run docs:rpc-index      (or: node scripts/generate-rpc-index.js)
// Reads:  data/modules/index.js       (must be built first: npm run build)
// Writes: <repo>/docs/RPC_INDEX_GENERATED.md
//
// Grouping is by leading name segment(s) — purely lexical, no semantics.

'use strict';

var fs = require('fs');
var path = require('path');

var MODULES_DIR = path.join(__dirname, '..');
var BUNDLE = path.join(MODULES_DIR, 'index.js');
var OUT = path.join(MODULES_DIR, '..', '..', 'docs', 'RPC_INDEX_GENERATED.md');

var src = fs.readFileSync(BUNDLE, 'utf8');

// Match both quote styles; postbuild guarantees literal ids.
var re = /registerRpc\(\s*['"]([^'"]+)['"]/g;
var ids = {};
var m;
while ((m = re.exec(src)) !== null) ids[m[1]] = true;

var all = Object.keys(ids).sort();

// Group by first underscore-delimited token; merge tiny groups into "misc".
var groups = {};
for (var i = 0; i < all.length; i++) {
  var id = all[i];
  var key = id.split('_')[0];
  (groups[key] = groups[key] || []).push(id);
}
var groupNames = Object.keys(groups).sort();
var big = [];
var misc = [];
for (var g = 0; g < groupNames.length; g++) {
  var name = groupNames[g];
  if (groups[name].length >= 3) big.push(name);
  else misc = misc.concat(groups[name]);
}
misc.sort();

var lines = [];
lines.push('# Nakama RPC Index (Auto-Generated)');
lines.push('');
lines.push('> **DO NOT EDIT BY HAND.** Generated by `data/modules/scripts/generate-rpc-index.js`');
lines.push('> from the compiled `data/modules/index.js`. Regenerate after every build with');
lines.push('> `npm run docs:rpc-index`. For parameter/response documentation see');
lines.push('> `COMPLETE_RPC_REFERENCE.md` — but THIS file is the authoritative answer to');
lines.push('> "does RPC X exist?" (see architecture doc §17.4 for why).');
lines.push('');
lines.push('**Generated:** ' + new Date().toISOString());
lines.push('**Total registered RPC ids:** ' + all.length);
lines.push('');

for (var b = 0; b < big.length; b++) {
  var gn = big[b];
  lines.push('## `' + gn + '_*` (' + groups[gn].length + ')');
  lines.push('');
  for (var r = 0; r < groups[gn].length; r++) lines.push('- `' + groups[gn][r] + '`');
  lines.push('');
}
lines.push('## Ungrouped / small prefixes (' + misc.length + ')');
lines.push('');
for (var u = 0; u < misc.length; u++) lines.push('- `' + misc[u] + '`');
lines.push('');

fs.writeFileSync(OUT, lines.join('\n'), 'utf8');
console.log('[rpc-index] Wrote ' + OUT);
console.log('[rpc-index] ' + all.length + ' unique RPC ids across ' + (big.length + 1) + ' groups');
